package com.operators;

public class Main {
    public static void main(String[] args) {

        // relational operators
        System.out.println(5 < 6);
        System.out.println(5 > 6);
        System.out.println(5 <= 6);
        System.out.println(5 >= 6);
        System.out.println(5 != 6);
        System.out.println(5 == 6);

        // logical operators
        System.out.println(!true);
        System.out.println(false || true);
        System.out.println(true && false);
    }
}
